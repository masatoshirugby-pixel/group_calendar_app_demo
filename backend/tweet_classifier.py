def classify_tweet(text: str) -> str | None:
    """
    ツイートテキストをイベントカテゴリに分類する。
    イベント性がない場合はNoneを返す。
    """
    lower = text.lower()
    for category in _PRIORITY:
        for word in CATEGORY_KEYWORDS[category]:
            if word.lower() in lower:
                return category
    return None
